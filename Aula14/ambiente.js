while (c <= 5) {
    console.log(c)
    c++ // c = c + 1 ;c += 1
}

//Maneira com o do 
var c = 1
do {
    console.log(c)
    c++ // c = c + 1 ; c += 1
}while (c <= 5) 

//----------------------------------------------------

